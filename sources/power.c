static void		copy_array(ULL *src, ULL *dest)
{
	int		i;

	i = 0;
	while (i < MAX_RANK && dest[i] != (ULL)(-1))
	{
		src[i] = dest[i];
		i++;
	}
	if (dest[i] == (ULL)(-1))
		src[i] = dest[i];
}

void			mult(ULL *m1, ULL *m2)
{
	ULL		result[MAX_RANK];
	ULL		tmp;
	int		i;
	int		j;

	fill_array_by_zero(result);
	j = 0;
	while (m2[j] != (ULL)(-1))
	{
		i = 0;
		tmp = 0;
		while (m1[i] != (ULL)(-1))
		{
			result[i + j] += m1[i] * m2[j] + tmp;
			tmp = result[i + j] / 1000000000;
			result[i + j] %= 1000000000;
			i++;
		}
		if (tmp)
			result[i + j] += tmp;
		j++;
	}
	result[i + j] = -1;
	copy_array(m1, result);
}

void			multiply(ULL *rank, int exp)
{
	int		i;

	i = 0;
	if (exp < 0)
	{
		exp *= -1;
		while (exp)
		{
			if (exp & 1)
				mult(rank, g_five[i]);
			exp = exp >> 1;
			i++;
		}
	}
	else
	{
		while (exp)
		{
			if (exp & 1)
				mult(rank, g_two[i]);
			exp = exp >> 1;
			i++;
		}
	}
}
